#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		len;

	i = 0;
	len = ft_strlen(s1);
	while (s1[i] && i < n)
	{
		s1[i + len] = s2[i];
		i++;
	}
	return (s1);
}
